export type TranslationKey = keyof typeof dictionary.id;

export function useT() {
  const language = usePreferencesStore((state) => state.language);
  return (key: TranslationKey, ...args: unknown[]): string => {
    const entry = dictionary[language][key] || dictionary.id[key];
    if (typeof entry === 'function') {
      return (entry as (...values: unknown[]) => string)(...args);
    }
    return entry;
  };
}

export function getLanguageLabel(language: AppLanguage) {
  return language === 'en' ? 'English' : 'Indonesia';
}
